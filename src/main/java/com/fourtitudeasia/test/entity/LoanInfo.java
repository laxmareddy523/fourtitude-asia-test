package com.fourtitudeasia.test.entity;

public class LoanInfo {

	private double loanPrinciple;
	private double fixedInterestRate;
	private int numberOfyears;

	public double getLoanPrinciple() {
		return loanPrinciple;
	}

	public void setLoanPrinciple(double loanPrinciple) {
		this.loanPrinciple = loanPrinciple;
	}

	public double getFixedInterestRate() {
		return fixedInterestRate;
	}

	public void setFixedInterestRate(double fixedInterestRate) {
		this.fixedInterestRate = fixedInterestRate;
	}

	public int getNumberOfyears() {
		return numberOfyears;
	}

	public void setNumberOfyears(int numberOfyears) {
		this.numberOfyears = numberOfyears;
	}

}
